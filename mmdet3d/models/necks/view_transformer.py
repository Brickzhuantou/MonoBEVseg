        # feature = self.horiconv(feature) # 不对图像特征做高度压缩
        # feature = feature.max(2)[0]
        # [N.112,W], [N,C,W]
        depth = depth.permute(0, 2, 3, 1).reshape(B, -1, self.D) # （B,NWH,D) 
        feature = feature.permute(0, 2, 3, 1).reshape(B, -1, self.out_channels)  # (B,NWH,C)
        circle_map, ray_map = self.get_proj_mat(input) # circle_map还是(B,D,hw); ray_map换成(B,NWH,hw); 

