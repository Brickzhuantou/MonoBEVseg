        (x, rots, trans, intrins, post_rots, post_trans, bda,
         mlp_input) = input[:8]

        B, N, C, H, W = x.shape
        x = x.view(B * N, C, H, W)
        x = self.depth_net(x, mlp_input)
        depth_digit = x[:, :self.D, ...]
        tran_feat = x[:, self.D:self.D + self.out_channels, ...]
        depth = depth_digit.softmax(dim=1)
        
        
        # 引入matrixVT替代view—transform
        feature = tran_feat
        img_feat_with_depth = self.reduce_and_project(
                feature, depth, input)  # [b*n, c, d, w]
        
        return img_feat_with_depth, depth
        
